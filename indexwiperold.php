$(function(){
  var ww = 0, //window width
      wh = 0, //window height
      bh = 0, //body height
      w = 0, //width
      h = 0, //body height - window height
      rad = 0, //radian
      deg = $(window).scrollTop()*(360/h),
      op1 = 0, //#p1 opacity
      op2 = 0, //#p2 opacity
      op3 = 0; //#p3 opacity

  $(window).on('load resize', function(e){
    ww = $(window).width();
    wh = $(window).height();
    if(wh > ww){w = wh;}else{w = ww;} 
    bh = $("body").height();
    h = bh - wh;
    $('#svg').css({
      'width':w*2 + 'px', 
      'height':w*2 + 'px', 
      'transform':'translate(' + -w/ 2 + 'px,' + -w + 'px) scale(1.2) ',
      'opacity':1
    });
    $('#svgwrapper').css({
      'height':w + 'px'
    });
    var fs = $('#svgwrapper p').css('fontSize');
    $('#svgwrapper p').css({
      'top':wh/2 - parseFloat(fs)*3 + 'px'
    });
    $('#p1').css({'opacity':1});
    $('#sd').css({
       'top':wh/10 * 9 + 'px'
    })
  });

  $(window).scroll(function(){
    /*
    How to rotate an html element with jquery on scroll percentage?
    http://stackoverflow.com/questions/19097437/how-to-rotate-an-html-element-with-jquery-on-scroll-percentage
    */
    deg = $(window).scrollTop()*(270/h);
    if(deg > 450){deg = 450;}
    $('#svg').css({
        "transform":'translate(' + -w/2 + 'px,' + -w + 'px) scale(1.2) ' + 'rotate('+ -deg+'deg)'
    });
        
    /*#p1*/
    if((deg < 30)){
      op1 = Math.round((1 - (1 / 30 * deg)) * 10) / 10;
    }else{
      op1 = 0;
    }
    $('#p1').css({'opacity':op1});
    
    /*#p2*/
    if((100 > deg) && (deg > 70)){
      op2 = Math.round((1 - (1 / (100 - 70) * (deg - 70))) * 10) / 10;
      op2 = 1 - op2;
    }else if((deg >=100) && (deg < 210)){
      op2 = 1;
    }else if((deg >=210) && (deg < 240)){
      op2 = Math.round((1 - (1 / (240 - 210) * (deg - 210))) * 10) / 10;
    }else{
      op2 = 0;
    }
    $('#p2').css({'opacity':op2});
    
    /*#p3*/
    if((310 >= deg) && (deg >280)){
      op3 = Math.round((1 - (1 / (310 - 280) * (deg - 280))) * 10) / 10;
      op3 = 1 - op3;
    }else if(deg > 310){
      op3 = 1;
    }else{
      op3 = 0;
    }
     $('#p3').css({'opacity':op3});
    
  });
});
